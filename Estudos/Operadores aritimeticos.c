#include <stdio.h>

int main() {
    int a = 10, b = 3;
    printf("Adição: %d\n", a + b);      // 13
    printf("Subtração: %d\n", a - b);   // 7
    printf("Multiplicação: %d\n", a * b); // 30
    printf("Divisão: %d\n", a / b);      // 3
    printf("Módulo: %d\n", a % b);       // 1
    return 0;
}

